    private void checar(java.awt.event.KeyEvent evt) {
        //cep 1,2
    	String check = "";
    	for (String s: jTextField1.getText().split("")) {
    		if (s.matches("[0-9]")) {
    			check+=s;
    		}
    	}
    	jTextField1.setText(check);
    	
    	check = "";
    	for (String s: jTextField2.getText().split("")) {
    		if (s.matches("[0-9]")) {
    			check+=s;
    		}
    	}
    	jTextField2.setText(check);
    	
    	check = "";
    	for (String s: jTextField3.getText().split("")) {
    		if (s.toLowerCase().matches("[a-z]")) {
    			check+=s;
    		}
    	}
    	jTextField3.setText(check);
    	
    	check = "";
    	for (String s: jTextField4.getText().split("")) {
    		if (s.toLowerCase().matches("[a-z]")) {
    			check+=s;
    		}
    	}
    	jTextField4.setText(check);
    	
    	check = "";
    	for (String s: jTextField5.getText().split("")) {
    		if (s.matches("[0-9]") || s.equals(".")) {
    			check+=s;
    		}
    	}
    	jTextField5.setText(check);
    	
    	check = "";
    	for (String s: jTextField6.getText().split("")) {
    		if (s.matches("[0-9]") || s.equals(".")) {
    			check+=s;
    		}
    	}
    	jTextField6.setText(check);
    }

