        		agencia.addEncomenda(agencia.criaEncomendaRegistrada(jTextField1.getText(), jTextField2.getText(), (new SimpleDateFormat("ddMMyyyy").format((new GregorianCalendar()).getTime())), nomeUsuario, jTextField3.getText(), jTextField4.getText(), Double.parseDouble(jTextField5.getText())));
